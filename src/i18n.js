const i18n = new I18n(translations);
i18n.enableFallback = true;
i18n.defaultLocale = 'en';

const initialLocale = Localization.getLocales?.()[0]?.languageCode || 'en';
i18n.locale = initialLocale;

export function getCurrentLanguage() {
  if (i18n.locale?.startsWith('hi')) return 'hi';
  if (i18n.locale?.startsWith('mr')) return 'mr';
  return 'en';
}

export async function initializeI18n() {
  try {
    const stored = await AsyncStorage.getItem(STORAGE_KEY);
    if (stored === 'en' || stored === 'hi' || stored === 'mr') {
      i18n.locale = stored;
    }
  } catch {
    // no-op: use device locale fallback
  }
}

export async function setLanguage(lang) {
  const next = lang === 'hi' ? 'hi' : lang === 'mr' ? 'mr' : 'en';
  i18n.locale = next;
  try {
    await AsyncStorage.setItem(STORAGE_KEY, next);
  } catch {
    // no-op: offline-first behavior still works for current runtime
  }
  listeners.forEach((fn) => fn(next));
}

export function subscribeLanguageChange(listener) {
  listeners.add(listener);
  return () => {
    listeners.delete(listener);
  };
}

export default i18n;
